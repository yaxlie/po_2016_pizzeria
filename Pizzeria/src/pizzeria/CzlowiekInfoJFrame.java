    public void zmienPojazd(){
        Dostawca d = (Dostawca) czlowiek;
        if("Samochód".equals(pojazdCB.getSelectedItem().toString()) && (d.getPojazd() instanceof Skuter))
            d.setZmienPojazd(true);
        else if("Samochód".equals(pojazdCB.getSelectedItem().toString()) && (d.getPojazd() instanceof Samochod))
            d.setZmienPojazd(false);
        else if("Skuter".equals(pojazdCB.getSelectedItem().toString()) && (d.getPojazd() instanceof Samochod))
            d.setZmienPojazd(true);
        else
            d.setZmienPojazd(false);
        System.out.println("Zmiana pojazdu dla " + d.getImie() + 
                " nastąpi przy najbiższym pobycie w pizzerii.");
    }
