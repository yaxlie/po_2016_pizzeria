    public void setInfo(){
        imieL.setText(czlowiek.getImie());
        nazwiskoL.setText(czlowiek.getNazwisko());
        peselL.setText(czlowiek.getPesel());
        typL.setText(czlowiek.getClass().getSimpleName());
        xL.setText(Integer.toString(czlowiek.getPolozenie().getX()));
        yL.setText(Integer.toString(czlowiek.getPolozenie().getY()));
        pojazdCB.setVisible(false);
        if(czlowiek instanceof Klient){
            Klient k = (Klient) czlowiek;
            extraL3.setText("Tel. : ");
            extraVL3.setText(k.getTelefon());
            extraL4.setText("Mail. : ");
            extraVL4.setText(k.getMail());
            powrotB.setVisible(false);
        }
        if(czlowiek instanceof KlientStaly){
            KlientStaly k = (KlientStaly) czlowiek;
            extraL.setText("P.Loj. : ");
            extraVL.setText(Float.toString(k.getpLojal()));
        }
            if(czlowiek instanceof KlientFirmowy){
            KlientFirmowy k = (KlientFirmowy) czlowiek;
            extraL.setText("Koresp. : ");
            extraVL.setText(k.getAdresKoresp());
            extraL1.setText("REGON : ");
            extraVL1.setText(k.getRegon());
            extraL2.setText("Konto : ");
            extraVL2.setText(k.getNrKontaBank());
        }
        if(czlowiek instanceof Dostawca){
            pojazdCB.setVisible(true);
            Dostawca d = (Dostawca) czlowiek;
            pojazdL.setText(d.getPojazd().getClass().getName());
            paliwoL.setText("Paliwo : " + Float.toString(d.getPojazd().getPaliwo()) + "/" +
                               Float.toString(d.getPojazd().getPojemoscBaku()));
            pojazdCB.removeAllItems();
            if(d.getUprawnieniaPojazd() == Dostawca.UprPojazd.Wszystkie){
                pojazdCB.addItem("Samochód");
                pojazdCB.addItem("Skuter");
                if((d.getPojazd() instanceof Skuter) || ((d.getPojazd() instanceof Samochod) &&
                        d.isZmienPojazd()))
                    pojazdCB.setSelectedIndex(1);
            }
            else if(d.getUprawnieniaPojazd() == Dostawca.UprPojazd.Skuter){
                pojazdCB.addItem("Skuter");
            }
            else
               pojazdCB.addItem("Samochód"); 
        }
    }

