    private void usunBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usunBMouseClicked
        // TODO add your handling code here:
        if(czlowiek instanceof Klient){
            pizzeria.getKlientL().remove(czlowiek.getImie() + " " + czlowiek.getNazwisko());
            czlowiek.setZyj(false);
        }
        else if(czlowiek instanceof Dostawca){
            System.out.println("Dostawca zostanie usunięty po dostarczeniu obecnego zamówienia.");
            Dostawca d = (Dostawca) czlowiek;
            d.setZwolniony(true);
           // pizzeria.getDostawcaL().remove(czlowiek.getImie() + " " + czlowiek.getNazwisko());
        }
        
        pizzeria.getMapa().repaint();
        this.setVisible(false);
    }//GEN-LAST:event_usunBMouseClicked

